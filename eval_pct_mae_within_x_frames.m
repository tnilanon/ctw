function eval_pct_mae_within_x_frames(mae_arr, x)

fprintf('(<= %d frames) begin: %.0f%%; subtask_2: %.0f%%; subtask_3: %.0f%%; end: %.0f%%\n', ...
    x, ...
    mean([mae_arr.begin] <= x) * 100, ...
    mean([mae_arr.subtask_2] <= x) * 100, ...
    mean([mae_arr.subtask_3] <= x) * 100, ...
    mean([mae_arr.end] <= x) * 100);